#include<iostream>
#include<queue>
using namespace std;

int main(){
	int a=10,b=2,c=0,d=-1,e=1;
	if(a++ and --b and c++ and d++){
		cout<<"hello"<<endl;
	}
	else if(++a and ++b and --c and e++){
		cout<<"world"<<endl;
	}
	else if(++a and b-- and c++ and --e){
		cout<<"coding "<<endl;
	}
	else{
		cout<<"blocks"<<endl;
	}
	cout<<a<<endl;
	cout<<b<<endl;
	cout<<c<<endl;
	cout<<d<<endl;
	cout<<e<<endl;




	return 0;
}